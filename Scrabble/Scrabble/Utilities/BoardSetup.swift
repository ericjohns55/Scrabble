enum BoardIdentifier {
    case diamond9, diamond11, diamond13, x9, x11, x13
    
    static func getBoardSize(_ identifier: BoardIdentifier) -> Int {
        switch identifier {
            case .diamond9, .x9:
                return 9
            case .diamond11, .x11:
                return 11
            case .diamond13, .x13:
                return 13
        }
    }
}

