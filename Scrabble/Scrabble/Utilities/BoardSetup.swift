            case .diamond9:
                boardTileValue = Diamond9x9[row][col]
                break
            case .x9:
                boardTileValue = X9x9[row][col]
                break
            case .diamond11:
                boardTileValue = Diamond11x11[row][col]
                break
            case .x11:
